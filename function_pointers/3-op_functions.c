#include "3-calc.h"

/**
 * op_add - add two int
 * @a: input a
 * @b: input b
 * Return: (a+b)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - get the difference between 2 int
 * @a: input a
 * @b: input b
 * Return: (a - b)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two int
 * @a: input a
 * @b: input b
 * Return: (a * b)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two int
 * @a: input a
 * @b: input b
 * Return: (a / b)
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get the remainder
 * @a: input a
 * @b: input b
 * Return: (a % b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
